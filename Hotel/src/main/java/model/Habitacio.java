package model;

public class Habitacio {
	int numhab,numpers;

	public Habitacio(int numhab, int numpers) {
		super();
		this.numhab = numhab;
		this.numpers = numpers;
	}

	public int getNumhab() {
		return numhab;
	}

	public void setNumhab(int numhab) {
		this.numhab = numhab;
	}

	public int getNumpers() {
		return numpers;
	}

	public void setNumpers(int numpers) {
		this.numpers = numpers;
	}


	

}
